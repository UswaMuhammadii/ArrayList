public class Frequency {
    String words;
    int count;

    public Frequency(String words, int count) {
        this.words = words;
        this.count = count;
    }

    public String getWords() {
        return words;
    }

    public int getCount() {
        return count;
    }

    public void setWords(String words) {
        this.words = words;
    }

    public void setCount(int count) {
        this.count = count;
    }
}

